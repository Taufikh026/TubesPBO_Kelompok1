    private void autonumber(){
        try{
            Connection c = Koneksi.getKoneksi();
            Statement s = c.createStatement();
            String sql = "SELECT * FROM loginmahasiswa ORDER BY NIM DESC";
            ResultSet r = s.executeQuery(sql);
            if (r.next()) {
                String NIM = r.getString("NIM").substring(3);
                String ID = "" +(Integer.parseInt(NIM)+1);
                String Zero = "";
                
                if (ID.length()==1) 
                {Zero = "00";}
                else if(ID.length()==2)
                {Zero = "0";}
                else if(ID.length()==3)
                {Zero = "";}
                
                FieldNIM.setText("AD" + Zero + ID);
            }else{
                FieldNIM.setText("AD001");
            }
            r.close();
            s.close();
        }catch(Exception e){
            System.out.println("autonumber error");
        }
    }

